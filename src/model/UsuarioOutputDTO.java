package model;

public class UsuarioOutputDTO {
	
	private int hashID;
	private String nome;
	private int idade;
	
	public int getHashID() {
		return hashID;
	}
	public void setHashID(int hashID) {
		this.hashID = hashID;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
	
	

}
