package pojo;

public class Usuario {
	
	private String nome;
	private int idade;
	private int HashId;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public int getHashId() {
		return HashId;
	}
	public void setHashId(int hashId) {
		HashId = hashId;
	}
	
	
	

}
